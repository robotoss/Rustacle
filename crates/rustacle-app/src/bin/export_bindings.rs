/// Generate TypeScript bindings from Rust IPC types.
/// Run: `cargo run -p rustacle-app --bin export_bindings`
fn main() {
    let builder = rustacle_app_lib::specta_builder();

    let bindings_path =
        std::path::Path::new(env!("CARGO_MANIFEST_DIR")).join("../../ui/bindings.ts");

    builder
        .export(
            specta_typescript::Typescript::default()
                .header("// This file is auto-generated by tauri-specta. DO NOT EDIT.\n"),
            &bindings_path,
        )
        .expect("failed to export TypeScript bindings");

    println!("Bindings exported to {}", bindings_path.display());
}
